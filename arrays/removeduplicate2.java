package arrays;

public class removeduplicate2 {
    public static int removedupli(int arr[]){
        int n = arr.length;
      
        int i = 0;
        int j = 0;
        while(j<n){
            int count = 0;
            if(arr[i] == arr[j]){
                count++;
                j++;
            }
            
        }
    }
    public static void main(String[] args) {
        int arr[] = {0,0,1,1,1,1,2,3,3};
    }

    
}
